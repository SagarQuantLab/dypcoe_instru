#LIST
# [], index, ordered, duplicates allowed, mutable


my_list = [1, 3, 5, 2, 8, 8]

# access first element
print(my_list[0])

# access second element
print(my_list[1])

# last element access
print(my_list[-1])

# second last element access
print(my_list[-2])

print(my_list)

# update first element
my_list[0] = 1000
print(my_list)